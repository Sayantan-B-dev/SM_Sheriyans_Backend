const mongoose = require('mongoose')

const productSchema = new mongoose.Schema({
    image: String,
    title: String,
    description: String,
    price: {
        ammount: {
            type: Number,
            min: 0,
        },
        currency: {
            type: String,
            default: "INR",
            enum: ["INR", "USD"]
        }
    }
})

const productModel=mongoose.model('product',productSchema)

module.exports={
    productModel
}